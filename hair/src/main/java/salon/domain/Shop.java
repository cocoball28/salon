package salon.domain;

public class Shop {
	private int no;
	private String name;
	private String phoName;
	
	public int getNo() {
		return no;
	}
	public void setNo(int no) {
		this.no = no;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getPhoName() {
		return phoName;
	}
	public void setPhoName(String phoName) {
		this.phoName = phoName;
	}
}
