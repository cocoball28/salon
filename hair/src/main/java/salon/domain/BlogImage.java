package salon.domain;

public class BlogImage {
	private String path;
	private int bpno;
	private int bno;

	public String getPath() {
		return path;
	}

	public void setPath(String path) {
		this.path = path;
	}

	public int getBpno() {
		return bpno;
	}

	public void setBpno(int bpno) {
		this.bpno = bpno;
	}

	public int getBno() {
		return bno;
	}

	public void setBno(int bno) {
		this.bno = bno;
	}

}
