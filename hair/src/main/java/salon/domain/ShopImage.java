package salon.domain;

public class ShopImage {
	private int spno;
	private int sano;
	private String path;
	public int getSpno() {
		return spno;
	}
	public void setSpno(int spno) {
		this.spno = spno;
	}
	public int getSano() {
		return sano;
	}
	public void setSano(int sano) {
		this.sano = sano;
	}
	public String getPath() {
		return path;
	}
	public void setPath(String path) {
		this.path = path;
	}
}
